import bf2, host, bf2.Timer, random
from game.gameplayPlugin import base
import game.utilities

ignore_subs = '25pdr mortar gwr34'.split()

def extract_cameras(obj):
    game.utilities.active(obj.templateName)
    type = game.utilities.getType()
    if type == 'camera':
        return [obj.templateName]
    else:
        out = []
        for c in obj.getChildren():
            out += extract_cameras(c)
        return out

class testing(base):
    def bf2_init(self, hooker):
        for pco in bf2.objectManager.getObjectsOfType('dice.hfe.world.ObjectTemplate.PlayerControlObject'):
            cams = extract_cameras(pco)
            for cam in cams:
                do_it = 1
                for x in ignore_subs:
                    if x in cam.lower(): do_it = 0
                if do_it:
                    game.utilities.active(cam)
                    game.utilities.rconExec('ObjectTemplate.CVMChase 1')
                    game.utilities.rconExec('ObjectTemplate.CVMFrontChase 1')
                    game.utilities.rconExec('ObjectTemplate.CVMFlyBy 1')